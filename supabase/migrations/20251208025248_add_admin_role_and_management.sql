/*
  # Add Admin Role and Management Capabilities

  1. Changes
    - Add admin role support to profiles table
    - Create policies for admin to manage teacher accounts
    - Allow admins to create profiles for teachers
    - Give admins full access to all platform data

  2. Security
    - Admins can create, read, update, and delete any profile
    - Admins can manage all questions, subjects, chapters, and categories
    - Regular users maintain their existing permissions
    
  3. Important Notes
    - First admin must be created manually via SQL or Supabase dashboard
    - To create the first admin, update an existing user's profile:
      UPDATE profiles SET role = 'admin' WHERE email = 'your-admin-email@example.com';
*/

-- Add policy for admins to insert any profile (for creating teacher accounts)
CREATE POLICY "Admins can insert any profile"
  ON profiles FOR INSERT
  TO authenticated
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE profiles.id = auth.uid()
      AND profiles.role = 'admin'
    )
  );

